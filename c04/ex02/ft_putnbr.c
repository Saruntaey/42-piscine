#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	c;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb < -9)
			ft_putnbr((nb / 10) * -1);
		ft_putnbr((nb % 10) * -1);
	}
	else if (nb < 10)
	{
		c = nb + '0';
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}

